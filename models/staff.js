function Staff(acount, name, email, password, workDay, salary, position, workHours, totalSalary, rating){
    this.acount = acount; 
    this.fullName = name; 
    this.email = email; 
    this.password = password; 
    this.workDay = workDay; 
    this.salary = salary; 
    this.position = position; 
    this.workHours = workHours; 
    this.totalSalary = totalSalary; 
    this.rating = rating; 
}